const spyEls = document.querySelectorAll('section.scroll-spy');
spyEls.forEach(function (spyEl) {
	new ScrollMagic
		.Scene({
			triggerElement: spyEl, // 보여짐 여부를 감사할 요소들
			triggerHook: .8
		})
		.setClassToggle(spyEl, 'show')
		.addTo(new ScrollMagic.Controller());
});


